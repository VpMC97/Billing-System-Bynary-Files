    private void bttAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttAgregar1ActionPerformed
        
        if (txCodigo.getText().isEmpty() || txNombre.getText().isEmpty() || 
            txDescripcion.getText().isEmpty() || txCantidad.getText().isEmpty() ||
            txPrecioU.getText().isEmpty() || txCodigoP.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, ingrese todos los campos");
        }
        else
        {
            Articulo_3597 a = new Articulo_3597(parseInt(txCodigo.getText()),
                                                  txNombre.getText(),
                                                  txDescripcion.getText(),
                                                  parseInt(txCantidad.getText()),
                                                  parseDouble(txPrecioU.getText()),
                                                  parseInt(txCodigoP.getText()));
            
            if (ab_Articulo3597.A_InsertarArticulo(a)){
                DefaultTableModel modelo = (DefaultTableModel) TablaArticulo.getModel();
                Object[] fila = new Object[6];
                fila[0] = a.getCodigo();
                fila[1] = a.getNombre();
                fila[2] = a.getDescripción();
                fila[3] = a.getCantidad();
                fila[4] = a.getPrecioUnitario();
                fila[5] = a.getcProveedor();
                modelo.addRow(fila);
            }
            limpiar();
        }
    }//GEN-LAST:event_bttAgregar1ActionPerformed

    private void bttModificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttModificar1ActionPerformed
        if (txCodigo.getText().isEmpty() || txNombre.getText().isEmpty() || 
            txDescripcion.getText().isEmpty() || txCantidad.getText().isEmpty() ||
            txPrecioU.getText().isEmpty() || txCodigoP.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, Ingrese todos los campos");
        }
        else{
            Articulo_3597 a = new Articulo_3597(parseInt(txCodigo.getText()),
                                                  txNombre.getText(),
                                                  txDescripcion.getText(),
                                                  parseInt(txCantidad.getText()),
                                                  parseDouble(txPrecioU.getText()),
                                                  parseInt(txCodigoP.getText()));
            
            if (ab_Articulo3597.A_ModificarArticulo(a)){
                int i = ab_Articulo3597.VerificarArticulo(a);
                
                TablaArticulo.setValueAt(a.getNombre(), i, 1);
                TablaArticulo.setValueAt(a.getDescripción(), i, 2);
                TablaArticulo.setValueAt(a.getCantidad(), i, 3);
                TablaArticulo.setValueAt(a.getPrecioUnitario(), i, 4);
                TablaArticulo.setValueAt(a.getcProveedor(), i, 5);
                
                limpiar();
            }
        }
    }//GEN-LAST:event_bttModificar1ActionPerformed

    private void bttEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttEliminar1ActionPerformed
        if (txCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, ingrese código de articulo que desea eliminar");
        }
        else{
            Articulo_3597 a = new Articulo_3597();
            a.setCodigo(parseInt(txCodigo.getText()));
            
            int i = ab_Articulo3597.VerificarArticulo(a);
            if (ab_Articulo3597.A_EliminarArticulo(a)){
                DefaultTableModel modelo = (DefaultTableModel) TablaArticulo.getModel();
                modelo.removeRow(i);
                limpiar();
            }
        }
    }//GEN-LAST:event_bttEliminar1ActionPerformed

