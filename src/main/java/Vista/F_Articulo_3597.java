    private void bttBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttBuscarActionPerformed
        if (txCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, ingrese código de artículo");
        }
        else
        {
            Articulo_3597 a = new Articulo_3597();
            a.setCodigo(parseInt(txCodigo.getText()));                    

            if (ab_Articulo3597.VerificarArticulo(a) != -1){

                int i = ab_Articulo3597.VerificarArticulo(a);
                A = ab_Articulo3597.A_inv();

                txNombre.setText(A.get(i).getNombre());
                txDescripcion.setText(A.get(i).getDescripción());
                txCantidad.setText(Integer.toString(A.get(i).getCantidad()));
                txPrecioU.setText(Double.toString(A.get(i).getPrecioUnitario()));
                txCodigoP.setText(Integer.toString(A.get(i).getcProveedor()));
                
                jta_Articulo.setText(A.get(i).toString());
            }
            else{
                JOptionPane.showMessageDialog(null, "Artículo no existente, según código de artículo\n\nPorfavor, inténtelo de nuevo");
                limpiar();
            }
        }
    }//GEN-LAST:event_bttBuscarActionPerformed

