    private void bttAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttAgregar1ActionPerformed
        
        if (txCodigo.getText().isEmpty() || txSalario.getText().isEmpty() ||
            cboxPuesto.getSelectedIndex() == 0 || txNombre.getText().isEmpty() || txApellido.getText().isEmpty() ||
            txDPI.getText().isEmpty() || txDireccion.getText().isEmpty() || txEdad.getText().isEmpty() ||
            txTelefono.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, ingrese / seleccione todos los campos");
        }
        else
        {
            Empleado e = new Empleado(parseInt(txCodigo.getText()), parseInt(lbCodID.getText()), 
                                      parseDouble(txSalario.getText()), cboxPuesto.getSelectedItem().toString(), 
                                      txNombre.getText(), txApellido.getText(), txDPI.getText(), 
                                      txDireccion.getText(), txEdad.getText(), txTelefono.getText());

            
            if (ab_Empleado.A_InsertarEmpleado(e)){
                DefaultTableModel modelo = (DefaultTableModel) TablaEmpleado.getModel();
                Object[] fila = new Object[10];
                fila[0] = e.getCodigo();
                fila[1] = e.getNombre();
                fila[2] = e.getApellido();
                fila[3] = e.getDPI();
                fila[4] = e.getDireccion();
                fila[5] = e.getEdad();
                fila[6] = e.getTelefono();
                fila[7] = e.getSalario();
                fila[8] = e.getID();
                fila[9] = e.getPuesto();
                
                modelo.addRow(fila);
            }
            limpiar();
        }
    }//GEN-LAST:event_bttAgregar1ActionPerformed

    private void bttModificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttModificar1ActionPerformed
        if (txCodigo.getText().isEmpty() || txSalario.getText().isEmpty() ||
            cboxPuesto.getSelectedIndex() == 0 || txNombre.getText().isEmpty() || txApellido.getText().isEmpty() ||
            txDPI.getText().isEmpty() || txDireccion.getText().isEmpty() || txEdad.getText().isEmpty() ||
            txTelefono.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, Ingrese todos los campos");
        }
        else{
            Empleado e = new Empleado(parseInt(txCodigo.getText()), parseInt(lbCodID.getText()), 
                                      parseDouble(txSalario.getText()), cboxPuesto.getSelectedItem().toString(), 
                                      txNombre.getText(), txApellido.getText(), txDPI.getText(), 
                                      txDireccion.getText(), txEdad.getText(), txTelefono.getText());
            
            if (ab_Empleado.A_ModificarEmpleado(e)){
                int i = ab_Empleado.VerificarEmpleado(e);
                
                TablaEmpleado.setValueAt(e.getNombre(), i, 1);
                TablaEmpleado.setValueAt(e.getApellido(), i, 2);
                TablaEmpleado.setValueAt(e.getDPI(), i, 3);
                TablaEmpleado.setValueAt(e.getDireccion(), i, 4);
                TablaEmpleado.setValueAt(e.getEdad(), i, 5);
                TablaEmpleado.setValueAt(e.getTelefono(), i, 6);
                TablaEmpleado.setValueAt(e.getSalario(), i, 7);
                TablaEmpleado.setValueAt(e.getID(), i, 8);
                TablaEmpleado.setValueAt(e.getPuesto(), i, 9);
                
                limpiar();
            }
        }
    }//GEN-LAST:event_bttModificar1ActionPerformed

