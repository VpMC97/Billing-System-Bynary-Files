    private void TableData(){
        if (ab_Empleado.A_inv() != null){
            E = ab_Empleado.A_inv();
             for (int o = 0; o < E.size(); o++) {
                DefaultTableModel modelo = (DefaultTableModel) TablaEmpleado.getModel();
                Object[] fila = new Object[10];
                fila[0] = E.get(o).getCodigo();
                fila[1] = E.get(o).getNombre();
                fila[2] = E.get(o).getApellido();
                fila[3] = E.get(o).getDPI();
                fila[4] = E.get(o).getDireccion();
                fila[5] = E.get(o).getEdad();
                fila[6] = E.get(o).getTelefono();
                fila[7] = E.get(o).getSalario();
                fila[8] = E.get(o).getID();
                fila[9] = E.get(o).getPuesto();
                
                modelo.addRow(fila); 
            }              
        }
    }
    
    
