    private void bttBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttBuscarActionPerformed
        if (txCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, ingrese código de empleado");
        }
        else
        {
             Empleado e = new Empleado();
             e.setCodigo(parseInt(txCodigo.getText()));
             
            if (ab_Empleado.VerificarEmpleado(e) != -1){
                
                int i = ab_Empleado.VerificarEmpleado(e);
                E = ab_Empleado.A_inv();

                txSalario.setText(Double.toString(E.get(i).getSalario()));
                cboxPuesto.setSelectedIndex(ab_Empleado.VerificarPuesto(e));
                lbCodID.setText(Integer.toString(E.get(i).getID()));
                txNombre.setText(E.get(i).getNombre());
                txApellido.setText(E.get(i).getApellido());
                txDPI.setText(E.get(i).getDPI());
                txDireccion.setText(E.get(i).getDireccion());
                txEdad.setText(E.get(i).getEdad());
                txTelefono.setText(E.get(i).getTelefono());
                
                jta_Empleado.setText(E.get(i).toString());
            }
            else{
                JOptionPane.showMessageDialog(null, "Empleado no existente, según código de empleado\n\nPorfavor, inténtelo de nuevo");
                limpiar();
            }
        }
    }//GEN-LAST:event_bttBuscarActionPerformed

