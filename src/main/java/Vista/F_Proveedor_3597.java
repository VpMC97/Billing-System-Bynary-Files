    private void bttBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttBuscarActionPerformed
        if (txCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, ingrese código de proveedor");
        }
        else
        {
            Proveedor_3597 p = new Proveedor_3597();
            p.setCodigo(parseInt(txCodigo.getText()));
            
            if (ab_Proveedor3597.VerificarProveedor(p) != -1){
                
                int i = ab_Proveedor3597.VerificarProveedor(p);
                P = ab_Proveedor3597.A_inv();

                txNombre.setText(P.get(i).getNombre());
                txNIT.setText(P.get(i).getNIT());
                txDireccion.setText(P.get(i).getDirección());
                txTelefono.setText(P.get(i).getTelefono());
                
                jta_Prov.setText(P.get(i).toString());
            }
            else{
                JOptionPane.showMessageDialog(null, "Proveedor no existente, según código de proveedor\n\nPorfavor, inténtelo de nuevo");
                limpiar();
            }
        }
    }//GEN-LAST:event_bttBuscarActionPerformed

