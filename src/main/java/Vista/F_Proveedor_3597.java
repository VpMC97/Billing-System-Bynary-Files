    private void bttAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttAgregar1ActionPerformed
        
        if (txCodigo.getText().isEmpty() || txNombre.getText().isEmpty() || 
            txNIT.getText().isEmpty() || txDireccion.getText().isEmpty() ||
            txTelefono.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, ingrese todos los campos");
        }
        else
        {
            Proveedor_3597 p = new Proveedor_3597(parseInt(txCodigo.getText()),
                                                  txNombre.getText(),
                                                  txNIT.getText(),
                                                  txDireccion.getText(),
                                                  txTelefono.getText());
            
            if (ab_Proveedor3597.A_InsertarProveedor(p)){
                DefaultTableModel modelo = (DefaultTableModel) TablaProveedor.getModel();
                Object[] fila = new Object[5];
                fila[0] = p.getCodigo();
                fila[1] = p.getNombre();
                fila[2] = p.getNIT();
                fila[3] = p.getDirección();
                fila[4] = p.getTelefono();
                modelo.addRow(fila);
                
                limpiar();
            }
        }
    }//GEN-LAST:event_bttAgregar1ActionPerformed

    private void bttModificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttModificar1ActionPerformed
        if (txCodigo.getText().isEmpty() || txNombre.getText().isEmpty() || 
            txNIT.getText().isEmpty() || txDireccion.getText().isEmpty() ||
            txTelefono.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, Ingrese todos los campos");
        }
        else{
            Proveedor_3597 p = new Proveedor_3597(parseInt(txCodigo.getText()),
                                                  txNombre.getText(),
                                                  txNIT.getText(),
                                                  txDireccion.getText(),
                                                  txTelefono.getText());
            
            if (ab_Proveedor3597.A_ModificarProveedor(p)){
                int i = ab_Proveedor3597.VerificarProveedor(p);
                
                TablaProveedor.setValueAt(p.getNombre(), i, 1);
                TablaProveedor.setValueAt(p.getNIT(), i, 2);
                TablaProveedor.setValueAt(p.getDirección(), i, 3);
                TablaProveedor.setValueAt(p.getTelefono(), i, 4);
                
                limpiar();
            }
        }
    }//GEN-LAST:event_bttModificar1ActionPerformed

    private void bttEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttEliminar1ActionPerformed
        if (txCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor, ingrese código de proveedor que desea eliminar");
        }
        else{
            Proveedor_3597 p = new Proveedor_3597();
            p.setCodigo(parseInt(txCodigo.getText()));
            
            int i = ab_Proveedor3597.VerificarProveedor(p);
            if (ab_Proveedor3597.A_EliminarProveedor(p)){
                DefaultTableModel modelo = (DefaultTableModel) TablaProveedor.getModel();
                modelo.removeRow(i);
                limpiar();
            }
        }
    }//GEN-LAST:event_bttEliminar1ActionPerformed

